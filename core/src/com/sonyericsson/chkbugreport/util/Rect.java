package com.sonyericsson.chkbugreport.util;

public class Rect {

    public int x, y, w, h;

    public Rect() {
        this(0, 0, 0, 0);
    }

    public Rect(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }

}
